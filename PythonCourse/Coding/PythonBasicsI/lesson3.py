# Math Functions

# round() remove the decimal, rounding the value.
print(round(2.1))

# abs() absolute value. No negative number
print(abs(-89))

# Operator Precedence
# (), **, /, *, -, +
print((20-3)+2**2)
print((5 + 4) * 10 / 2)

print(((5 + 4) * 10) / 2)

print((5 + 4) * (10 / 2))

print(5 + (4 * 10) / 2)

print(5 + 4 * 10 // 2)
# Coversion in to Binary
print(bin(5))

# Convert binary into base 2
print(int('0b101',2)) # first arguement will be binary value and 2nd arguement will be base